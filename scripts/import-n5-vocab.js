function translateMeaning(meaning) {
  const source = meaning.trim();
  if (exactMeaningZh[source]) return exactMeaningZh[source];
  for (const [pattern, replacement] of phraseZh) {
    if (pattern.test(source)) return source.replace(pattern, replacement);
  }
  if (/^to /.test(source)) return translateVerb(source);
  return source
    .split(/\s*,\s*/)
    .map((part) => translatePart(part))
    .join("；");
}

