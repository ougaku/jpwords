function translatePart(part) {
  const cleaned = part.trim();
  if (exactMeaningZh[cleaned]) return exactMeaningZh[cleaned];
  if (tokenZh[cleaned]) return tokenZh[cleaned];
  const parenthetical = cleaned.match(/^(.+?)\s*\((.+)\)$/);
  if (parenthetical) {
    return `${translatePart(parenthetical[1])}（${translatePart(parenthetical[2])}）`;
  }
  return cleaned.replace(/[A-Za-z][A-Za-z .'-]*/g, (token) => tokenZh[token.trim()] || token.trim());
}

function toWord(row, index) {
  const [kanji, furigana, romaji, meaning] = row;
  const kana = primaryKana(furigana);
  const surface = (kanji || kana).trim();
  const meaningEn = meaning.trim();

  return {
    id: stableId(index),
    japanese: surface,
    kana,
    meaning: translateMeaning(meaningEn),
    meaningEn,
    part: "词汇",
    level: "N5",
    example: "",
    translation: "",
    tags: ["JLPT", "N5", "MIT数据", "自动中文释义", romaji.trim()].filter(Boolean),
    status: "published",
    version: 1,
    access: "free",
  };
}

function parseWords(csv) {
  const words = [];
  const seen = new Set();
  const lines = csv.replace(/^\uFEFF/, "").split(/\r?\n/);

  for (const line of lines) {
    if (!line.trim()) continue;
    const row = parseCsvLine(line);
    if (row.length < 4) continue;
    if (row[0] === "Kanji" && row[1] === "Furigana") continue;
    if (!row[1] || !row[3]) continue;

    const key = `${row[0]}|${row[1]}|${row[3]}`;
    if (seen.has(key)) continue;
    seen.add(key);
    words.push(toWord(row, words.length + 1));
  }

  return words;
}

